    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        DateTimeFormatter date = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate fechanacimiento = LocalDate.parse(txtfechanacimiento.getText(), date);
        LocalDate fechaactual = LocalDate.now();
        
        Period periodo = Period.between(fechanacimiento, fechaactual);
        
        String resultado = (" "+ periodo.getYears() + " AÃ±os " + periodo.getMonths() + " Meses " + periodo.getDays() + " Dias");
        txtrespuesta.setText(resultado);
        
    }//GEN-LAST:event_btncalcularActionPerformed

    private void txtfechanacimientoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfechanacimientoKeyTyped
        // TODO add your handling code here:
        char validar = evt.getKeyChar();
        
        if (Character.isLetter(validar)){
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(rootPane, "PLEASE, ONLY ENTER NUMBERS");
        }
    }//GEN-LAST:event_txtfechanacimientoKeyTyped

